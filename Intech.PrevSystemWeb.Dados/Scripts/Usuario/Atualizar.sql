/*Config
    Retorno
        -void
    Parametros
        -USR_CODIGO:int
        -USR_LOGIN:string
        -USR_SENHA:string
        -USR_ADMINISTRADOR:string
        -USR_TIPO_EXPIRACAO:string
        -USR_NOME:string
        -USR_EMAIL:string
        -CD_PESSOA:int
