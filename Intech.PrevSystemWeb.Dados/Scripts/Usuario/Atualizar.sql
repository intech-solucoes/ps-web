UPDATE FR_USUARIO 
SET USR_CODIGO = @USR_CODIGO,
    USR_LOGIN = @USR_LOGIN,
    USR_SENHA = @USR_SENHA,
    USR_ADMINISTRADOR = @USR_ADMINISTRADOR,
    USR_TIPO_EXPIRACAO = @USR_TIPO_EXPIRACAO,
    USR_NOME = @USR_NOME,
    USR_EMAIL = @USR_EMAIL,
    CD_PESSOA = @CD_PESSOA,
    EE_TERMO_RESPONSABILIDADE = @EE_TERMO_RESPONSABILIDADE,
    CD_PESSOA_CLIENTE = @CD_PESSOA_CLIENTE
WHERE USR_CODIGO = @USR_CODIGO